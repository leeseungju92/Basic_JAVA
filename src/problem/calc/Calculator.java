package problem.calc;

public class Calculator {
	int result;

	// 덧셈기능
	public int sum(int first, int second) {
		
		return first + second;
	}

	public int sub(int first, int second) {
		return first - second;
	}

	public int mux(int first, int second) {
		return first * second;
	}

	public int div(int first, int second) {
		return first / second;

	}

}
